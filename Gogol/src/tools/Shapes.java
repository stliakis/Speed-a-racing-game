		public static void registerShape(String name,float[] vertices,short[] indices){
			shapes.put(name, new Shape(vertices,indices));
		}
		public static void registerShape(String name,double[] vertices,short[] indices){
			float[] vers=new float[vertices.length];
			for(int c=0;c<vertices.length;c++){
				vers[c]=(float) vertices[c];
			}
			shapes.put(name, new Shape(vers,indices));
		}
		public static Shape getShape(String name){
			return shapes.get(name);
		}
		
